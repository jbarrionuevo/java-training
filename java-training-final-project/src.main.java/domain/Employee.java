package domain;

import persistence.DAO.DTO;

public abstract class Employee implements DTO {
	public String name;

	private String type;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
