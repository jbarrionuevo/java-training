package edu.globant.ioncases.model;

/**
 *
 * @author federico.calarco
 */
public abstract class Product {
    
    private Long id;
    private double price;
    private int stock;

    public Product() {
    }

    public Product(double price, int stock) {
        this.price = price;
        this.stock = stock;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
    
    
    
    
   
}
