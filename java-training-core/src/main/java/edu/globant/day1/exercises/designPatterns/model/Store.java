package edu.globant.day1.exercises.designPatterns.model;

public class Store {
 
	private int id;
	private String name;
	
	public Store(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
